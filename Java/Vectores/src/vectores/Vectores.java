    private void btnCargarVectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarVectorActionPerformed
        // TODO add your handling code here:
        //int[] calif = new int[7];
        //int suma = 0;
        //double promedio;

        //calif[0] = 10;
        //calif[1] = 9;
        //calif[2] = 8;
        //calif[3] = 7;
        //calif[4] = 6;
        //calif[5] = 9;
        //calif[6] = 8;
        //for (int i = 0; i < 7; i++){
        //cboCalif.addItem(i + "[" + calif[i] + "]");
        //suma = suma + calif[i];
        //}
        //promedio = suma / 7;
        int[] calif;
        double suma = 0;
        int tamano;
        int max, xuma = 0;
        int mayor=0;
        int menor=100;
        double promedio;
        tamano = Integer.parseInt(txtTamano.getText());
        calif = new int[tamano];
        
        for (int i = 0; i < tamano; i++) {
            calif[i] = Integer.parseInt(JOptionPane.showInputDialog("La calificasion es:"));
            cboCalif.addItem(i+1 + " [" + calif[i] + "]");
            suma = suma + calif[i];
            if(calif[i]>mayor){
                mayor=calif[i];
            }
            if(calif[i]<menor){
                menor=calif[i];
            }
        }
        txtMax.setText(Integer.toString(mayor));
        txtMenor.setText(Integer.toString(menor));
        promedio = suma / tamano;
        txtPromedio.setText(Double.toString(promedio));
    }//GEN-LAST:event_btnCargarVectorActionPerformed

